<p> <strong>・繰り上げ返済手数料</strong>：ローンの繰り上げ返済には手数料が必要になります。住宅ローンには&ldquo;繰り上げて手数料無料&rdquo;というサービスを行なっている業者もあるのですが、&ldquo;一括繰り上げ返済&rdquo;に関しては有料という住宅ローンが殆どです。ローン乗り換えには抵当権などの問題から一括繰り上げ返済が不可欠です。現在利用している住宅ローンの一括繰り上げ返済の手数料を調べておきましょう。</p>
<p><strong>・契約書貼付印紙税</strong>：借り替えローンは新たに住宅ローンを借り入れるので契約書に添付する収入印紙（印紙税）が必要です。必要な印紙税は以下の通りです。<br />
  借入額１０００万円超５０００万円以下…２万円<br />
  借入額５０００万円超１億円以下…６万円</p>
<p><strong>・抵当権設定費用</strong>：新たな住宅ローンを組むにあたり抵当権を抹消し新たに抵当権を設定する必要があります。登録免許税は借入額の０．１％、また登記を行なう上で司法書士報酬が別途必要です。<br />
  </p>
<p>住宅ローン乗り換えは借入額によっても異なりますが、２０００万円の借り入れで５０万円程の費用が必要です。        </p>
