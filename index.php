        <p><!--現在の金利の比較を見て頂きましたが、-->そもそも住宅ローンというものは、住宅の建築費や購入代金があまりに高額で、現金による一括払いが不可能なため、ローンを組んで建築費や購入代金をねん出しようというものです。 <br />
          <br />
          ですから、<em>利息の分だけ、一括払いよりも高くつくのは、仕方のないこと</em>なのです。 </p>
        <p>そこで大切になってくるのが、住宅ローンの金利です。 <br />
          <strong>金利が低ければ、当然、利息の額も少なくて済みます</strong>から、どの程度の金利で住宅ローンを組むのかということは、ご家族の生活設計にも関わる、大変重要な問題と言えます。 </p>
          
          
                   
          
        <p>ただ、やっかいなのは、<strong>金利というものは上下する</strong>ということです。</p>
        <p>特に固定金利の場合には、金利が高い時期に住宅ローンを組んでしまいますと、あとあと非常に苦労します。</p>
        <p>また、変動金利の住宅ローンに固定金利特約を付けて利用される場合にも、金利が安い時期に住宅ローンを組んでおくことで、最初の固定金利期間の利息を安くすることができるため、結果的にはお得になってきます。 </p>
        <p>ちなみに、<strong>住宅ローンの返済総額を減らすための方法として有力なのが、住宅ローンの繰り上げ返済</strong>です。</p>
        <p>繰り上げ返済を行なえば、その分、元本を減らすことができます。元本を減らすことができれば、その分、利息の額も減るため、返済総額を減らす上で、非常に有効な手段なのです。 </p>
        <p>しかし、繰り上げ返済をするためには先立つものが必要です。 <br />
          金利の高い住宅ローンを組んでしまった場合、利息が高いために、繰り上げ返済用のお金をなかなか貯めることができません。</p>
        <p>結果、<strong>繰り上げ返済ができず、利息の額を減らすこともできないといった、悪循環</strong>になりがちなのです。 </p>
        <p>以上のように、住宅ローンを組む上で金利は非常に重要な要素です。そして、金利というものは上下するものであるがゆえに、金利が安い時に住宅ローンを組むことが、とても大切になってくるわけです。 </p>
        <p>でも、本当に金利というものはそんなに上下するものなのか？　そう疑問に思われる方もいらっしゃるかも知れません。</p>
