<h4>【住宅ローン比較ポイント②～金利タイプを知る～】</h4>
<p>金利を比較する上で金利のタイプを知るということが大切です。住宅ローンには<em>変動金利タイプ</em>と<em>固定金利タイプ</em>という二つのタイプがあります。</p>
<p>どのような保険も基本的にこの二つのタイプに分類することができます。<br />
  では、変動金利タイプと固定金利タイプにはどのような特徴があるのでしょうか？</p>
<h5>変動金利タイプ</h5>
  <p>変動金利タイプは<strong>市場によって金利が変化する</strong>という特徴があります。<br />
    市場の変化によって金利が上がることもあれば、金利が下がることもることから、<em>変動金利タイプは投資的な一面</em>を持っています。変動金利は市場の変化による金利変化のリスクを利用者が負うことから、固定金利に比べて&ldquo;<strong>金利が低い</strong>&rdquo;という利点があります。<br />
    変動金利タイプの最大のメリットは、超低金利社会においては非常に低い金利で融資を受けられるということです。デメリットは、市場の変化によって<strong>ローンの支払い総額の見通しが立たない</strong>ということです。</p>
  <h5>固定金利タイプ</h5>
  <p>固定金利タイプは<strong>契約時の金利を維持できる</strong>という特徴があります。<br />
    契約時に設定した金利をローン完済時、または一定期間維持できるので<em>ローンの返済計画が立てやすい</em>という利点があります。<br />
    固定金利最大の最大のメリットはローンの借入額を確定可能で、さらに将来金利が上昇しても金利が変化しないということです。デメリットは、変動金利に比べて&ldquo;<strong>金利が高い</strong>&rdquo;ということです。 </p>
  <p> 金利タイプに関してはそれぞれにメリットとデメリットがあります。&ldquo;どちらがお得なのか？&rdquo;ということに関してはローンを完済してみるまでは分からないというのが現実です。</p>
  <p>どのように決断するのかということは<strong>自分のリスクの許容に応じて決定する</strong>ということが大切です。ローン商品によっては<strong><em>ミックス金利タイプ（金利タイプの組み合わせ）</em></strong>を選べるということもあります。</p>
  </p>
